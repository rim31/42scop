#include "libftprintf.h"

int		ft_isconv(int c)
{
	if (c != 's' && c != 'S' && c != 'p'
		&& c != 'd' && c != 'D' && c != 'o'
		&& c != 'O' && c != 'u' && c != 'U'
		&& c != 'x' && c != 'X' && c != 'c'
		&& c != 'C' && c != 'i' && c != '%')
		return (0);
	else
		return (1);
}
